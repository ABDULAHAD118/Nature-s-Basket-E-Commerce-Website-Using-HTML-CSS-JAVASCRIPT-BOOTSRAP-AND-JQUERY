let cart = [];

// Add event listeners to the add-to-cart buttons
document.querySelectorAll('.add-to-cart-btn').forEach(button => {
    button.addEventListener('click', handleAddToCart);
});

// Function to handle adding items to the cart
function handleAddToCart(event) {
    event.preventDefault(); // Prevent default link behavior

    const productId = event.target.getAttribute('data-product-id');
    const productName = event.target.getAttribute('data-product-name');
    const productPrice = parseFloat(event.target.getAttribute('data-product-price'));
    const productImage = event.target.getAttribute('data-product-image');

    console.log(`Adding item: ID=${productId}, Name=${productName}, Price=${productPrice}, Image=${productImage}`);

    const cartItem = cart.find(item => item.id == productId);

    if (cartItem) {
        cartItem.quantity += 1;
    } else {
        cart.push({ id: productId, name: productName, price: productPrice, quantity: 1, image: productImage });
    }

    // Save the cart to local storage
    localStorage.setItem('cart', JSON.stringify(cart));
    // cartlength();
}

// Function to calculate and display the cart length
function cartLength() {
    let len = 0; // Default length is 0
    if (localStorage.cart) {
        const a = JSON.parse(localStorage.cart);
        len = a.length;
    }
    document.getElementById('num').textContent = len;
}

// Load the cart length when the window loads
window.addEventListener('load', cartLength);
